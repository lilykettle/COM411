first_number = int(input("enter a whole number"))
second_number = int(input("enter another whole number"))
if first_number > second_number:
    print(first_number , "is bigger than" , second_number)

elif first_number < second_number:
    print(second_number , "is bigger than" , first_number)

else:
    print(first_number , "is equal to" , second_number)
